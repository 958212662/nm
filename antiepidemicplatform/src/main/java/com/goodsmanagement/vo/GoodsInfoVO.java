package com.goodsmanagement.vo;

import com.entities.GoodsInfoDO;

import java.io.Serializable;

/**
 * @Description
 * @Author: linchong
 * @Date: 2018/11/5 16:57
 * @Version 1.0
 */

public class GoodsInfoVO extends GoodsInfoDO implements Serializable {

    private String goodsTypeName;   //物资类型
    private String distributorName; //发放单位
    private String receiverName;    //接收人姓名(特指乡级指定的防疫员的姓名)
    private String orgName;         //接收组织名
    private Integer userId;         //用于前端判断“修改”，“删除”操作是否显示


    public String getGoodsTypeName() {
        return goodsTypeName;
    }

    public void setGoodsTypeName(String goodsTypeName) {
        this.goodsTypeName = goodsTypeName;
    }

    public String getDistributorName() {
        return distributorName;
    }

    public void setDistributorName(String distributorName) {
        this.distributorName = distributorName;
    }

    public String getReceiverName() {
        return receiverName;
    }

    public void setReceiverName(String receiverName) {
        this.receiverName = receiverName;
    }

    public String getOrgName() {
        return orgName;
    }

    public void setOrgName(String orgName) {
        this.orgName = orgName;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }
}
