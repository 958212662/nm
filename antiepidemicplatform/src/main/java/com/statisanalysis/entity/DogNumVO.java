package com.statisanalysis.entity;

/**
 * @Description 犬只数量统计
 * @Author: linchong
 * @Date: 2018/9/30 16:04
 * @Version 1.0
 */
public class DogNumVO {
    private String perMonth;
    private Integer liveNum;  //存栏数量
    private Integer newNum;   //新增数量
    private Integer deadNum;  //死亡数量
    private Integer cancelNum; //注销数量

    public DogNumVO() {
    }

    public DogNumVO(String perMonth, Integer liveNum, Integer newNum, Integer deadNum, Integer cancelNum) {
        this.perMonth = perMonth;
        this.liveNum = liveNum;
        this.newNum = newNum;
        this.deadNum = deadNum;
        this.cancelNum = cancelNum;
    }

    public String getPerMonth() {
        return perMonth;
    }

    public void setPerMonth(String perMonth) {
        this.perMonth = perMonth;
    }

    public Integer getLiveNum() {
        return liveNum;
    }

    public void setLiveNum(Integer liveNum) {
        this.liveNum = liveNum;
    }

    public Integer getNewNum() {
        return newNum;
    }

    public void setNewNum(Integer newNum) {
        this.newNum = newNum;
    }

    public Integer getDeadNum() {
        return deadNum;
    }

    public void setDeadNum(Integer deadNum) {
        this.deadNum = deadNum;
    }

    public Integer getCancelNum() {
        return cancelNum;
    }

    public void setCancelNum(Integer cancelNum) {
        this.cancelNum = cancelNum;
    }

    @Override
    public String toString() {
        return "DogNumVO{" +
                "perMonth='" + perMonth + '\'' +
                ", liveNum=" + liveNum +
                ", newNum=" + newNum +
                ", deadNum=" + deadNum +
                ", cancelNum=" + cancelNum +
                '}';
    }
}
