package com.soholy.cb.entity.iot;

import java.io.Serializable;

public class AccessToken implements Serializable {
  private static final long serialVersionUID = -7929395629918359231L;
  
  private String scope;
  
  private String tokenType;
  
  private Integer expiresIn;
  
  private String accessToken;
  
  private String refreshToken;
  
  public String toString() { return "AccessToken [scope=" + this.scope + ", tokenType=" + this.tokenType + ", expiresIn=" + this.expiresIn + ", accessToken=" + this.accessToken + ", refreshToken=" + this.refreshToken + "]"; }
  
  public String getScope() { return this.scope; }
  
  public void setScope(String scope) { this.scope = scope; }
  
  public String getTokenType() { return this.tokenType; }
  
  public void setTokenType(String tokenType) { this.tokenType = tokenType; }
  
  public Integer getExpiresIn() { return this.expiresIn; }
  
  public void setExpiresIn(Integer expiresIn) { this.expiresIn = expiresIn; }
  
  public String getAccessToken() { return this.accessToken; }
  
  public void setAccessToken(String accessToken) { this.accessToken = accessToken; }
  
  public String getRefreshToken() { return this.refreshToken; }
  
  public void setRefreshToken(String refreshToken) { this.refreshToken = refreshToken; }
}
