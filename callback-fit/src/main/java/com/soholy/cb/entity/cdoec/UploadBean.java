package com.soholy.cb.entity.cdoec;

import java.util.Date;

public class UploadBean<T> extends UploadBeanV1_5 {
  private T t;
  
  private String imei;
  
  private Double electricQuantity;
  
  private String factoryNo;
  
  private String deviceType;
  
  private Date upLoadTime;
  
  public T getT() {
    return (T)this.t;
  }
  
  public void setT(T t) { this.t = t; }
  
  public String getImei() { return this.imei; }
  
  public void setImei(String imei) { this.imei = imei; }
  
  public Double getElectricQuantity() { return this.electricQuantity; }
  
  public void setElectricQuantity(Double electricQuantity) { this.electricQuantity = electricQuantity; }
  
  public String getFactoryNo() { return this.factoryNo; }
  
  public void setFactoryNo(String factoryNo) { this.factoryNo = factoryNo; }
  
  public String getDeviceType() { return this.deviceType; }
  
  public void setDeviceType(String deviceType) { this.deviceType = deviceType; }
  
  public Date getUpLoadTime() { return this.upLoadTime; }
  
  public void setUpLoadTime(Date upLoadTime) { this.upLoadTime = upLoadTime; }
}
